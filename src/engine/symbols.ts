let path2DCache = new Map<number, Path2D>();

function getPath(num: number): Path2D | undefined {
  if (path2DCache.has(num)) return path2DCache.get(num);
  const def = SYMBOLS[num];
  if (!def) return undefined;
  const p = new Path2D(def.path);
  path2DCache.set(num, p);
  return p;
}

/** 在 Canvas 上绘制指定符号，自动缩放至目标尺寸并填色/描边 */
export function drawSymbol(
  ctx: CanvasRenderingContext2D,
  num: number,
  x: number, y: number,
  size: number,
  color: string,
) {
  const def = SYMBOLS[num];
  const path = getPath(num);
  if (!def || !path) return;
  const scale = size / Math.max(def.vw, def.vh);
  ctx.save();
  ctx.translate(x, y);
  ctx.scale(scale, scale);
  ctx.translate(-def.cx, -def.cy);
  if (def.stroke) {
    ctx.strokeStyle = color;
    ctx.lineWidth = 1.2 / scale;
    ctx.stroke(path);
  } else {
    ctx.fillStyle = color;
    ctx.fill(path);
  }
  ctx.restore();
}
